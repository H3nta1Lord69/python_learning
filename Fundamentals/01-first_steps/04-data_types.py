
# Data types in Python
# Python has several built-in data types to store different kinds of data.
# Here are some of the most common data types:
# 1. Integer (int): Represents whole numbers.
age = 30
print(age)  # This will print 30, which is an integer.
# 2. Float (float): Represents decimal numbers.
height = 5.9
print(height)  # This will print 5.9, which is a float.
# 3. String (str): Represents text data.
name = "Alice"
print(name)  # This will print "Alice", which is a string.
# 4. Boolean (bool): Represents True or False values.
is_student = True
print(is_student)  # This will print True, which is a boolean.
# 5. List: Represents an ordered collection of items.
fruits = ["apple", "banana", "cherry"]
print(fruits)  # This will print the list of fruits.
# 6. Tuple: Represents an ordered, immutable collection of items.
coordinates = (10.0, 20.0)
print(coordinates)  # This will print the tuple of coordinates.
# 7. Dictionary (dict): Represents a collection of key-value pairs.
person = {"name": "Bob", "age": 25}
print(person)  # This will print the dictionary representing a person.
# You can check the type of a variable using the type() function:
print(type(age))        # This will print <class 'int'>
print(type(height))     # This will print <class 'float'>
print(type(name))       # This will print <class 'str'>
print(type(is_student)) # This will print <class 'bool'>
print(type(fruits))     # This will print <class 'list'>
print(type(coordinates)) # This will print <class 'tuple'>
print(type(person))     # This will print <class 'dict'>
# Understanding data types is crucial for effective programming in Python.
# Different data types have different properties and methods associated with them.
# You can perform various operations based on the data type.
# For example, you can concatenate strings, perform arithmetic on numbers, and manipulate lists.
# Always choose the appropriate data type for the data you want to store.
# This helps ensure that your code behaves as expected and is easy to read and maintain.
# Remember that Python is dynamically typed, so you can change the type of a variable by assigning a new value of a different type:
data = 100          # Initially an integer
print(type(data))   # This will print <class 'int'>
data = "Now I'm a string"  # Now a string
print(type(data))   # This will print <class 'str'>
# This flexibility allows for dynamic and versatile programming in Python!
# However, be cautious when changing data types, as it may lead to unexpected behavior if not handled properly.
# Always test your code to ensure it works as intended after changing data types.
